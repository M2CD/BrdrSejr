            <h2>Brdr. Sejr</h2> 
                <p>
Erik og Eigil Sejr som stiftede det nuværende BRDR. SEJR MOTORCYKLER, er begge udlært hos ÅBO Motor først i tresserne, og de opkøbte ÅBO Motor, da de starteded Dyssehøj Biler i Brabrand.</p>
                <p>
                Igennem tiderne har Brdr. Sejr haft mange lærlinge. Da rette tid og alder var, kom lillebror Niels Sejr til som arbejdsdreng, og senere i lære som mekaniker, og stod udlært i 1978. Nu var Brødrene fuldtallige!</p>
                <p>
Omkring ti år senere i 1988 blev Benny Haslev udlært, og har været i firmaet siden. I maj 1998 blev Erik Sejr's datter Tina Sejr ansat, og skulle gå som "føl" hos Eigil Sejr i butikken. Desværre går Eigil Sejr pludselig bort i Juni 1998, og den daglige drift af butikken overgår til Tina Sejr, som bliver kastet hovedkuls ud i det, efter kun to måneders ansættelse.</p>
                <p>
Tina Sejr, Niels Sejr og Benny Haslev indgår herefter som medejere af Brdr.Sejr Motorcykler ApS sammen med Erik Sejr.</p>
                <p>
I 2011 går Erik Sejr og hans kone Linda Sejr på pension: Linda Sejr som gennem alle årene har været den faste "klippe" på kontoret. Erik og Linda er dog stadig tilknyttet firmaet i form af ejerskabet af bygningerne.</p>
        
